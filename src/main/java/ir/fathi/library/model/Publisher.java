package ir.fathi.library.model;

public class Publisher {
    private long id;
    private String title;

    public Publisher() {
    }

    public Publisher(long id, String title) {
        this.id = id;
        this.title = title;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
